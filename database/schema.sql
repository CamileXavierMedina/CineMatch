-- COMPETE AO RAFAEL DESENVOLVER O SCRIPT SQL E CRIAR A TABELA NO SUPABASE

-- REQUISITOS OBRIGATORIOS PARA CRIACAO DA TABELA:
-- 1. A TABELA DEVE CHAMAR-SE EXACTAMENTE: filmes_salvos
-- 2. DEVE CONTER UMA CHAVE PRIMARIA AUTO-GERADA (SUGESTAO: UUID COM VALOR PADRAO)
-- 3. DEVE CONTER UM CAMPO INTEIRO, INDISPENSAVEL E UNICO PARA O ID DO FILME DO TMDB (EX: tmdb_id)
-- 4. DEVE CONTER UM CAMPO DE TEXTO OBRIGATORIO PARA O TITULO DO FILME
-- 5. DEVE CONTER CAMPOS DE TEXTO PARA A SINOPSE, ANO DE LANCAMENTO E CAMINHO DO POSTER (POSTER_PATH)
-- 6. DEVE CONTER UM CAMPO DE TEXTO OBRIGATORIO PARA CONTROLAR O STATUS (quero OU assistido)
-- 7. DEVE CONTER UM CAMPO INTEIRO PARA ARMAZENAR A NOTA DE 1 A 5 (PADRAO ZERO)
-- 8. DEVE CONTER UM CAMPO DE DATA DE REGISTRO COM PREENCHIMENTO AUTOMATICO DO HORARIO DO SUPABASE